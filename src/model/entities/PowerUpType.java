package model.entities;

/**
 * The type of a {@link PowerUp}.
 */
public enum PowerUpType {
    /**
     * The end of level zone.
     */
    GOAL,
    /**
     * Add an extra life to player.
     */
    EXTRA_LIFE,
    /**
     * Player moves faster and is invincible for a short while.
     */
    INVINCIBILITY;
}
